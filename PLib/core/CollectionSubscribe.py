class CollectionSubscribe:

    def __init__(self,
                 methodName,
                 methodObj,
                 methodMessageType):
        self.methodName
        self.methodObj
        self.methodMessageType

    def get_MethodName(self):
        return self.methodName

    def get_MethodObj(self):
        return self.methodObj

    def get_MethodMessageType(self):
        return self.methodMessageType